  ZDQL2=ZQSEUIL (JLON, JLEV+1)*(ZQVS (JLON, JLEV+1)-ZQVSH2)
  ZDH2=ZCPH (JLON, JLEV+1)*(ZTDERI-PT (JLON, JLEV+1))+ZLH&
  & (JLON, JLEV+1)*ZDQL2-ZRH (JLON, JLEV+1)*ZTDERI*ZLOG
