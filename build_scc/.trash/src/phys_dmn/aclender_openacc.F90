  ZDQL2=ZQSEUIL (JLON, JLEV)*(ZQVS (JLON, JLEV)-ZQVSH2)
  ZDH2=ZCPH (JLON, JLEV)*(ZTDERI-PT (JLON, JLEV))+ZLH (JLON, JLEV)*ZDQL2-ZRH (JLON, JLEV)*ZTDERI*ZLOG
  ZTHDC (JLON, JLEV)=ZTJ+ZDH1*(ZTDERI-ZTJ)/(ZDH1-ZDH2)
  ZTHD (JLON, JLEV)=ZTJ
  
