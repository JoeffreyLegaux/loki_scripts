    ZEW2=FOEW (ZTLF2, ZH2)/PAPRSF (JLON, JLEV+1)
    ZQSATF2=FOQS (ZEW2)
    ZDLEWF2=FODLEW (ZTLF2, ZH2)
    ZQSLTLF2=FODQS (ZQSATF2, ZEW2, ZDLEWF2)
