DO JLEV=1, KLEV
  
  ZSEUILM=MAX (0.0_JPRB, SIGN (1.0_JPRB, ZRIF (JLON, JLEV)))
  ZSEUILH=MAX (0.0_JPRB, SIGN (1.0_JPRB, ZRIF (JLON, JLEV)))
