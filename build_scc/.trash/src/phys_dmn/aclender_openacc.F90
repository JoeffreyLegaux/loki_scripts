  ZRIF (JLON, JLEV)=0.5_JPRB*(ZRI (JLON, JLEV-1)+ZRI (JLON, JLEV))
  ZRIF2 (JLON, JLEV)=0.5_JPRB*(ZRI2 (JLON, JLEV-1)+ZRI2 (JLON, JLEV))
  
