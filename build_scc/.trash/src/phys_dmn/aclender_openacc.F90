  DO JLEV=1, KLEV
    
    ZLENDM (JLON, JLEV)=ZLENDM_CLEAR (JLON, JLEV)
    ZLENDH (JLON, JLEV)=ZLENDH_CLEAR (JLON, JLEV)
  
