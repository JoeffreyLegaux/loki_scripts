    ZTF2=PT (JLON, JLEV+1)
    ZQCF2=PQLI (JLON, JLEV+1)+PQICE (JLON, JLEV+1)
    ZQWF2=PQV (JLON, JLEV+1)+PQLI (JLON, JLEV+1)+PQICE (JLON, JLEV+1)
