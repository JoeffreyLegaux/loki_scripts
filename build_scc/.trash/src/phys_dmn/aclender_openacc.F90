    ZQSLTLH=(ZQSLTLF1+ZQSLTLF2)/2.0_JPRB
    ZLSCPEH=(ZLSCPEF1+ZLSCPEF2)/2.0_JPRB
    ZDELTQH (JLON, JLEV)=(ZDELTQF1+ZDELTQF2)/2.0_JPRB
    ZTHETAH=ZTH*(YDCST%RATM/PAPRS (JLON, JLEV))**YDCST%RKAPPA
