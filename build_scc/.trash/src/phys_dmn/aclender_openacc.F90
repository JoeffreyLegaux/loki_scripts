  ZLOG=LOG (PAPRS (JLON, JLEV)/PAPRSF (JLON, JLEV+1))
  ZQVSH1=FOQS (FOEW (ZTJ, ZNEIGE)/PAPRS (JLON, JLEV))
  ZDQL1=ZQSEUIL (JLON, JLEV+1)*(ZQVS (JLON, JLEV+1)-ZQVSH1)
