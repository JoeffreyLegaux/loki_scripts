  & JLEV))+YDCST%RCPV*ZQV (JLON, JLEV)+YDCST%RCW*ZQL (JLON, JLEV)
  ZLH (JLON, JLEV)=YDCST%RLVTT-(PT (JLON, JLEV)-YDCST%RTT)*(YDCST%RCW-YDCST%RCPV)
